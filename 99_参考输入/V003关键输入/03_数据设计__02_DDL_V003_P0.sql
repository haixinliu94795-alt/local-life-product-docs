  UNIQUE KEY uk_ccs_clearing_idempotent (idempotent_key),
  KEY idx_ccs_clearing_order (business_scene, source_order_no),
  KEY idx_ccs_clearing_merchant (merchant_id, create_time),
  KEY idx_ccs_clearing_event (source_event_no)
