-- 清结算平台 P0 DDL - 普通商品落地准入版
-- Database: MySQL 8.x
-- 约定：
-- 1. 金额字段统一 DECIMAL(18,2)，比例字段 DECIMAL(8,4)。
-- 2. 时间字段统一 DATETIME(3)。
-- 3. P0 不包含冻结/止付字段和状态。
-- 4. 逻辑删除字段 deleted：0=未删除，1=已删除。
-- 5. tenant_id 预留多租户，单租户可固定为 'default'。

CREATE TABLE IF NOT EXISTS ccs_source_event (
  id BIGINT NOT NULL AUTO_INCREMENT COMMENT '自增主键',
  tenant_id VARCHAR(32) NOT NULL DEFAULT 'default' COMMENT '租户ID',
  event_no VARCHAR(64) NOT NULL COMMENT '清结算平台事件号',
  source_system VARCHAR(64) NOT NULL COMMENT '来源系统，如 LOCAL_LIFE_ORDER',
  business_scene VARCHAR(64) NOT NULL COMMENT '业务场景，如 LOCAL_LIFE_NORMAL',
  event_type VARCHAR(64) NOT NULL COMMENT '事件类型：WRITE_OFF_COMPLETED/REFUND_SUCCESS/WRITE_OFF_CANCELED',
  source_event_id VARCHAR(128) NOT NULL COMMENT '来源事件唯一ID',
  source_biz_no VARCHAR(128) NOT NULL COMMENT '来源业务单号，如订单号/核销单号/退款单号',
  source_order_no VARCHAR(128) DEFAULT NULL COMMENT '来源订单号',
  event_time DATETIME(3) NOT NULL COMMENT '业务事件发生时间',
  idempotent_key VARCHAR(256) NOT NULL COMMENT '事件幂等键',
  payload_json JSON NOT NULL COMMENT '原始事件载荷',
  process_status VARCHAR(32) NOT NULL DEFAULT 'INIT' COMMENT 'INIT/PROCESSING/SUCCESS/FAILED/IGNORED/WAITING',
  process_count INT NOT NULL DEFAULT 0 COMMENT '处理次数',
  next_retry_time DATETIME(3) DEFAULT NULL COMMENT '下次重试时间',
  last_error_code VARCHAR(64) DEFAULT NULL COMMENT '最后错误码',
  last_error_message VARCHAR(512) DEFAULT NULL COMMENT '最后错误信息',
  create_time DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3),
  update_time DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3) ON UPDATE CURRENT_TIMESTAMP(3),
  deleted TINYINT NOT NULL DEFAULT 0 COMMENT '逻辑删除',
  PRIMARY KEY (id),
  UNIQUE KEY uk_ccs_source_event_no (event_no),
  UNIQUE KEY uk_ccs_source_event_idempotent (idempotent_key),
  UNIQUE KEY uk_ccs_source_event_source (source_system, source_event_id),
  KEY idx_ccs_source_event_biz (business_scene, event_type, source_biz_no),
  KEY idx_ccs_source_event_status (process_status, next_retry_time),
  KEY idx_ccs_source_event_order (source_order_no)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='清结算来源事件表';

CREATE TABLE IF NOT EXISTS ccs_clearing_result (
