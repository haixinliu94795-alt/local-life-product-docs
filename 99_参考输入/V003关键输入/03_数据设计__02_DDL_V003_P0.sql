  write_off_no VARCHAR(128) DEFAULT NULL COMMENT '核销单号',
  merchant_id VARCHAR(64) NOT NULL COMMENT '商户ID',
  merchant_name VARCHAR(128) DEFAULT NULL COMMENT '商户名称快照',
  currency VARCHAR(8) NOT NULL DEFAULT 'CNY',
  direction VARCHAR(16) NOT NULL DEFAULT 'POSITIVE' COMMENT 'POSITIVE/NEGATIVE',
  order_amount DECIMAL(18,2) NOT NULL DEFAULT 0.00 COMMENT '订单金额',
